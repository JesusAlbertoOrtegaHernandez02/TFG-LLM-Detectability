def compute_burstiness(text, tokenizer, model):

    # dividir en frases
    sentences = re.split(r'[.!?]+', text)
    sentences = [s.strip() for s in sentences if len(s.strip()) > 10]

    if len(sentences) < 2:
        return 0

    ppls = []

    for s in sentences:

        inputs = tokenizer(
            s,
            return_tensors="pt",
            truncation=True,
            max_length=512
        ).to(model.device)

        with torch.no_grad():
            outputs = model(**inputs, labels=inputs["input_ids"])
            loss = outputs.loss

        ppl = torch.exp(loss).item()
        ppls.append(ppl)

    return np.std(ppls)

