def radar_score(text: str) -> float:
    inputs = radar_tok(text, return_tensors="pt",
                       truncation=True, max_length=512).to(device)
    with torch.no_grad():
        probs = torch.softmax(radar_model(**inputs).logits, dim=-1)
    return float(probs[0, 1].item())

def chatgpt_detector_score(text: str) -> float:
    inputs = det_tok(
        text,
        return_tensors="pt",
        truncation=True,
        max_length=512
    ).to(device)

    with torch.no_grad():
        probs = torch.softmax(det_model(**inputs).logits, dim=-1)

    return float(probs[0, 1].item())

def gltr_mean_rank(text: str) -> float:
    enc = gltr_tok(text, return_tensors="pt", truncation=True, max_length=512)
    input_ids = enc["input_ids"].to(device)

    with torch.no_grad():
        out = gltr_model(input_ids)
        logits = out.logits[:, :-1, :]
        labels = input_ids[:, 1:]

    probs = torch.softmax(logits, dim=-1)[0]
    labels = labels[0]

    ranks = []
    for i in range(labels.shape[0]):
        tok_id = labels[i].item()
        rank = int(torch.sum(probs[i] > probs[i, tok_id]).item())
        ranks.append(rank)

    return float(np.mean(ranks)) if ranks else float("nan")

