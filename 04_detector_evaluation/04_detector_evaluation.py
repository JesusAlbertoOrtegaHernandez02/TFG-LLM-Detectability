# Se utilizan Cohen's d y Kolmogorov-Smirnov para medir separación entre distribuciones.
from scipy.stats import ks_2samp
import numpy as np

def cohens_d(x, y):
    x = np.array(x)
    y = np.array(y)
    nx = len(x)
    ny = len(y)
    pooled_std = np.sqrt(((nx - 1)*np.var(x, ddof=1) + (ny - 1)*np.var(y, ddof=1)) / (nx + ny - 2))
    return (np.mean(x) - np.mean(y)) / pooled_std

def compare_distributions(df_human, df_model, metric_name):
    x = df_human[metric_name].dropna()
    y = df_model[metric_name].dropna()

    d = cohens_d(x, y)
    ks_stat, ks_p = ks_2samp(x, y)

    return {
        "metric": metric_name,
        "cohens_d": d,
        "ks_statistic": ks_stat,
        "ks_p_value": ks_p
    }

